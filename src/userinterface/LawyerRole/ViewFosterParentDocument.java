    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(btnApproval.isSelected() && btnDisapproval.isSelected()){
            JOptionPane.showMessageDialog(null, "Please select one of the options");
        }
        else{
        if(btnApproval.isSelected()){
            request.setApproval(Boolean.TRUE);
                        JOptionPane.showMessageDialog(null, "Approved");
                        request.setLawyer(userAccount.getUsername());
        }
        else if(btnDisapproval.isSelected()){
            request.setApproval(Boolean.FALSE);
                        JOptionPane.showMessageDialog(null, "Disapproved");
                        request.setLawyer(userAccount.getUsername());

        }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

